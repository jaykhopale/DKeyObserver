package com.dkey.observe;

public class Observation {

	private String id;
	private String comments;
	private String common_name;
	private String family;
	private String genus;
	private String latitude;
	private String longitude;
	private String species;
	private String morefields1;
	private String morefields2;
	private String morefields3;

	public Observation(){
	
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getComments() {
		return comments;
	}

	public void setComments(String comments) {
		this.comments = comments;
	}

	public String getCommon_name() {
		return common_name;
	}

	public void setCommon_name(String common_name) {
		this.common_name = common_name;
	}

	public String getFamily() {
		return family;
	}

	public void setFamily(String family) {
		this.family = family;
	}

	public String getGenus() {
		return genus;
	}

	public void setGenus(String genus) {
		this.genus = genus;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public String getSpecies() {
		return species;
	}

	public void setSpecies(String species) {
		this.species = species;
	}

	public String getMorefields1() {
		return morefields1;
	}

	public void setMorefields1(String morefields1) {
		this.morefields1 = morefields1;
	}

	public String getMorefields2() {
		return morefields2;
	}

	public void setMorefields2(String morefields2) {
		this.morefields2 = morefields2;
	}

	public String getMorefields3() {
		return morefields3;
	}

	public void setMorefields3(String morefields3) {
		this.morefields3 = morefields3;
	}




}
